    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            if(txtNumeroHab.getText().isEmpty()){
               JOptionPane.showMessageDialog(this,"ingresa numero para eliminar"); 
               limpiarcontroles();
               txtNumeroHab.requestFocus();
            } else {
                objHab.eliminarhabitacion(Integer.parseInt(txtNumeroHab.getText()));
                JOptionPane.showMessageDialog(this,"Habitacion eliminada");
                limpiarcontroles();
                habilitarbotones(false);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

