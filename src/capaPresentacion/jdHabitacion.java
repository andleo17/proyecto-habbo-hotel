    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
     ResultSet hab;
    String est="";
   
        try {
            if(txtNumeroHab.getText().isEmpty()){
                JOptionPane.showMessageDialog(this,"Debe de ingresar un número de habitación a buscar");
            }else{
                hab=objHab.consultarHabitacion(Integer.parseInt(txtNumeroHab.getText()));
               
                if(hab.next()){
                    if(hab.getString("estado").equals("D")) est="Disponible";
                    if(hab.getString("estado").equals("O")) est="Ocupada";
                    if(hab.getString("estado").equals("M")) est="Mantenimiento";
                    txtDescripcion.setText(hab.getString("descripcion"));
                    cmbEstado.setSelectedItem(est);
                    chkVigencia.setSelected(hab.getBoolean("vigencia"));
                    cmbTipoHab.setSelectedItem(hab.getString("nombre"));
                    habilitarbotones(true);
                }else{
                    JOptionPane.showMessageDialog(this,"El número de habitacion no existe,ingrese uno válido");
                    habilitarbotones(true);
                    txtNumeroHab.requestFocus();
                    limpiarcontroles();
                    
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }  
    }//GEN-LAST:event_btnBuscarActionPerformed

