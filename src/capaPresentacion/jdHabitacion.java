    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
       String est="";
        if (btnNuevo.getText().equals("Nuevo")){
           btnNuevo.setText("Guardar");
           limpiarcontroles() ; 
           txtNumeroHab.requestFocus();
           
       } else { 
           btnNuevo.setText("Nuevo");
           try {
               if(objHab.verificanumhab(Integer.parseInt(txtNumeroHab.getText()))){
                   if(cmbEstado.getSelectedItem().equals("Disponible")) est="D";
                   if(cmbEstado.getSelectedItem().equals("Ocupada")) est="O";
                   if(cmbEstado.getSelectedItem().equals("Mantenimiento")) est="M";
                   objHab.registarHabit(Integer.parseInt(txtNumeroHab.getText()),txtDescripcion.getText(),est,chkVigencia.isSelected(),objTipohab.retornarCodigoTipoHabitacion(cmbTipoHab.getSelectedItem().toString()));
                   JOptionPane.showMessageDialog(this,"Se registro");
                   limpiarcontroles();
                   txtNumeroHab.requestFocus();
                   listarhabitacion();
           } else { 
                   JOptionPane.showMessageDialog(this,"Numero habitacion ya esta registrada");
                   limpiarcontroles();
                   txtNumeroHab.requestFocus();
                   
               }
           } catch (Exception e) {
                JOptionPane.showMessageDialog(this,e.getMessage());
           }
       }
    }//GEN-LAST:event_btnNuevoActionPerformed

