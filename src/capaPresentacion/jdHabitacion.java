    private void listartipohab() {
        ResultSet rsTipoHab= null; 
        DefaultComboBoxModel modelTipoHab = new DefaultComboBoxModel() ;
        cmbTipoHab.setModel(modelTipoHab);
        try {
            rsTipoHab =  objTipohab.listarTiphabvigente();
            while ( rsTipoHab.next()) {
                modelTipoHab.addElement(rsTipoHab.getString("nombre"));
            }
                
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }
    
    private void listarhabitacion() { 
       String estado ="";
       String vigencia = "No Vigente"; 
        ResultSet rsHabitacion = null ;
        DefaultTableModel modelh= new DefaultTableModel(); 
        modelh.addColumn("NUMERO");
        modelh.addColumn("DESCRIPCION");
        modelh.addColumn("ESTADO");
        modelh.addColumn("VIGENCIA");
        modelh.addColumn("TIPO HABITACION");
        tblHabitacion.setModel(modelh);
        try {
            rsHabitacion=objHab.listarHab();
            while(rsHabitacion.next()){
                if (rsHabitacion.getString("estado").equals("D")) estado = "Disponible";
                if (rsHabitacion.getString("estado").equals("O")) estado = "Ocupada";
                if (rsHabitacion.getString("estado").equals("M")) estado = "Mantenimiento";
                if (rsHabitacion.getBoolean("vigencia")) vigencia="Vigente";
                modelh.addRow(new Object[]{rsHabitacion.getInt("numerohab"),rsHabitacion.getString("descripcion"), estado,vigencia, rsHabitacion.getString("nombre")});
                vigencia="No vigente";
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this,e.getMessage());
        }
        
    }
