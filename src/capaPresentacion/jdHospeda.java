    private void cmbTipoHabActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbTipoHabActionPerformed
        ResultSet rsHabitacion = null;
        DefaultTableModel modeloHab = new DefaultTableModel();
        modeloHab.addColumn("Nro Habitación");
        modeloHab.addColumn("Tipo Habitación");
        modeloHab.addColumn("Costo");
        tblHabitacion.setModel(modeloHab);
        try {
            rsHabitacion= objhab.listarHabDis(cmbTipoHab.getSelectedItem().toString());
            while(rsHabitacion.next()){
                modeloHab.addRow(new Object[]{rsHabitacion.getInt("numerohab"),rsHabitacion.getString("nombre"),rsHabitacion.getDouble("precio")});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_cmbTipoHabActionPerformed

