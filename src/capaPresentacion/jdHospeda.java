    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        try {
            if (btnNuevo.getText().equals("Nuevo")){
                btnNuevo.setText("Guardar");
                txtNumHospedaje.setText(String.valueOf( objHos.generarNumeroHospedaje()));
            }else{
                btnNuevo.setText("Nuevo");
                objHos.registrarHospedaje(Integer.parseInt(txtNumHospedaje.getText()),lblFecha.getText(),txtMotivo.getText(),Double.parseDouble("0.0"),Integer.parseInt(txtNumHabitacion.getText()),txtDni.getText(),dniEmpleado);
                objhab.cambiarEstadoHabitacion(Integer.parseInt(txtNumHabitacion.getText()), "O");
                limpiarControles();
                JOptionPane.showMessageDialog(this, "Hospedaje registrado correctamente!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnBuscarHospedajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarHospedajeActionPerformed
        ResultSet rsHospedaje=null;
        try {
            if (txtNumHospedaje.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Ingrese número de hospedaje a consultar...");
            }else{
                rsHospedaje= objHos.buscarHospedajes(Integer.parseInt(txtNumHospedaje.getText()));
                if (rsHospedaje.next()){
                    txtDni.setText(rsHospedaje.getString("dnihue"));
                    btnBuscarHuespedActionPerformed(evt);
                    txtNumHabitacion.setText(String.valueOf( rsHospedaje.getInt("numerohab")));
                    cmbTipoHab.setSelectedItem(objhab.obtenerNombreTipoHabitacion(rsHospedaje.getInt("numerohab")));
                    //cmbTipoHab.setSelectedItem(String.valueOf(objHab.obtenerNombreTipoHabitacion(101)));
                    txtMotivo.setText(rsHospedaje.getString("motivo"));
                }else{
                    JOptionPane.showMessageDialog(this, "Número de hospedaje no registrado!");
                    limpiarControles();
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnBuscarHospedajeActionPerformed

