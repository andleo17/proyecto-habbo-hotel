    private void btnBuscarHuespedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarHuespedActionPerformed
        try {
            if(txtDni.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Debe ingresar un DNI a buscar...");
            }else{
                rsHuesped= objhues.buscarHuesped(txtDni.getText());
                if(rsHuesped.next()){
                    txtNombre.setText(rsHuesped.getString("nombres"));
                    txtApellidos.setText(rsHuesped.getString("apellidos"));
                }else{
                    JOptionPane.showMessageDialog(this, "DNI de huésped no está registrado!");
                    limpiarHuesped();
                    btnNuevoHuesped.setEnabled(true);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarHuespedActionPerformed

